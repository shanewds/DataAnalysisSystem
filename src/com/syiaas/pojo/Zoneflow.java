package com.syiaas.pojo;

public class Zoneflow {
    private long id;
    private int zoneid;
    private double zoneflow;
    private double zonetime;
	public long getId() {
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}
	public int getZoneid() {
		return zoneid;
	}
	public void setZoneid(int zoneid) {
		this.zoneid = zoneid;
	}
	public double getZoneflow() {
		return zoneflow;
	}
	public void setZoneflow(double zoneflow) {
		this.zoneflow = zoneflow;
	}
	public double getZonetime() {
		return zonetime;
	}
	public void setZonetime(double zonetime) {
		this.zonetime = zonetime;
	}
    
    
    
    
}
