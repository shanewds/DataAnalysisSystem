package com.syiaas.utils;

public class LineDataUtil {
    private String name;
    private Integer[] data;
    
	public LineDataUtil() {
		super();
	}
	
	public LineDataUtil(String name, Integer[] data) {
		super();
		this.name = name;
		this.data = data;
	}

	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Integer[] getData() {
		return data;
	}
	public void setData(Integer[] data) {
		this.data = data;
	}
    
    
    
	
    
    
}
